# Deze regel telt 15 en 4 op: 15 + 4 = 19
print(15 + 4)
# Deze regel trekt 4 af van 15: 15 - 4 = 11
print(15 - 4)
# Deze regel vermenigvuldigt 15 met 4: 15 * 4 = 60
print(15 * 4)
# Deze regel deelt 15 door 4, resulterend in een float: 15 / 4 = 3.75
print(15 / 4)
# Deze regel voert een gehele deling uit van 15 door 4: 15 // 4 = 3
print(15 // 4)
# Deze regel verheft 15 tot de macht 4: 15 ** 4 = 50625
print(15 ** 4)
# Deze regel vindt de rest van de deling van 15 door 4: 15 % 4 = 3
print(15 % 4)