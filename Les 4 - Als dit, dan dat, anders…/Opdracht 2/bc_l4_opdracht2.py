getal1 = int(input('Wat is de eerste getal? '))
getal2 = int(input('Wat is de tweede getal? '))

if getal1 > getal2:
    print(f'{getal1} is grooter dan {getal2}')
elif getal1 < getal2:
    print(f'{getal1} is kleiner dan {getal2}')
elif getal1 == getal2:
    print(f'{getal1} is gelijk aan {getal2}')